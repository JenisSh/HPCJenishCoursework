    n = j - (100 * m);

    if (n == 0 || m == 0 || n == width - 1 || m == height - 1) {
      out[j] = 0;
    } else {
      b = j + 100;
      d = j - 1;
      f = j + 1;
      h = j - 100;

      r = (in[j] * 4) + (in[b] * -1) + (in[d] * -1) + (in[f] * -1) + (in[h] * -1);

      if (r > 0) { 
        out[j] = 255;
      } else {
        out[j] = 0;
      }
    }
  }




